// The statement 3+4; adds the integers 3 and 4 together. Given that the
// result isn't stored in a variable nor fed to a function, the statement
// is effectually doing nothing.

#include <iostream>

int main()
{
    std::cout << "\nThe statement 3+4; adds the integers 3 and 4 together." << std::endl;
    std::cout << "\n\t\t3+4 = " << 3+4 << std::endl << std::endl;
    std::cout << "Given that the result isn't stored in a variable nor fed to" << std::endl;
    std::cout << "a function, the statement is effectually doing nothing.\n" << std::endl;
    return 0;
}
